#include "libft.h"

int	ft_atoi(const char *str)
{
	int	out;
	int	sign;
	int	i;

	i = 0;
	out = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		out = out * 10 + str[i] - 48;
		i++;
	}
	return (sign * out);
}
